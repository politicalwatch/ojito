      <h3>Compromiso cumplido (C)</h3>

      <p>Consideramos un compromiso cumplido cuando, de acorde con la (1) ambición y (2) contenido de dicho compromiso, el partido político o el Gobierno lo hayan impulsado al máximo nivel en función de su rol en el Congreso de los Diputados.</p>
      <p>Por ejemplo, cojamos el caso del Gobierno y su compromiso con las migraciones:</p>
      <blockquote>
        <p>Promoveremos una política europea de inmigración justa y solidaria a través de la articulación de vías legales y seguras, que respete los derechos humanos y garantice las libertades y principios de la UE para todas las personas.</p>
        <p>Para ello se modificará el sistema nacional de acogida e inclusión de solicitantes de Protección Internacional, adaptándolo a la normativa europea y a la nueva realidad del país para hacerlo más eficiente y solidario.  Se dotará el Fondo estatal de integración como instrumento esencial de colaboración del Estado con las comunidades autónomas y los municipios en este ámbito.</p>
      </blockquote>

      <p class="list-cap">En este caso, se valorará este compromiso como cumplido si:</p>
      <ul>
        <li>Se impulsan las medidas comprometidas <strong>al más alto nivel al alcance</strong> del actor al que estemos analizando. En el supuesto de que el Gobierno haya presentado un proyecto de ley se valorará el contenido de éste en relación con el compromiso adquirido. Además, si se dispone de información,  se valorará  la actividad de negociación con el resto de grupos parlamentarios para conseguir los apoyos necesarios para conseguir su aprobación.</li>
      </ul>

      <p>En el caso de los <strong>partidos políticos</strong>, se valorará como cumplido cuando su actividad se lleve a cabo con la ambición expuesta en el acuerdo/ compromiso previo.  En el caso de los partidos también se evaluarán sus votos durante la tramitación de los proyectos de ley, proposiciones de ley, enmiendas o PNL de los otros grupos parlamentarios en las temáticas analizadas.</p>

      <p>Por ejemplo, en el caso del compromiso en el programa electoral del Partido Popular, observamos que hay un compromiso en torno al cuidado/conciliación:</p>
      <blockquote>
        <p>Favoreceremos la conciliación de la vida laboral, personal y familiar a través de un Pacto Nacional para la Conciliación Laboral y la Racionalización de Horarios, que incluya, entre otras, medidas sobre teletrabajo, bolsas de horas para asuntos propios o finalización de la jornada laboral</p>
      </blockquote>
      <p>En este caso, se monitorizará si, por un lado, está intentando promover este Pacto Nacional o una proposición de ley en torno a la conciliación. Además, también se valorará el apoyo que pueda dar a las medidas del Gobierno u otros partidos sobre esta misma temática.</p>

      <h3>Parcialmente cumplido (PC)</h3>

      <p>Un compromiso se considerará como parcialmente cumplido si se ha producido cierto cumplimiento pero con un menor nivel de ambición de lo que se había comprometido.</p>
      <p>Por ejemplo, en el caso en el que un partido se haya comprometido a promover una ley sobre una cuestión relevante para la justicia social y climática pero finalmente su nivel de ambición en este aspecto decrece y la iniciativa que impulsa tiene menor relevancia y por tanto peso para alcanzar cambios reales.</p>
      <p>Un ejemplo claro sería tomar como referencia la siguiente propuesta de Unidas Podemos «Crear un impuesto para las grandes fortunas, con el fin de recaudar un 1 % del PIB de patrimonios superiores a un millón de euros, y de forma progresiva. Se trata de un impuesto similar al que se está proponiendo para la UE y Estados Unidos. Tendrá un mínimo exento por primera vivienda de 400 000 euros y gravará con un 2 % los patrimonios de más de un millón de euros, con un 2,5 % los patrimonios de más de 10 millones de euros, con un 3 % los patrimonios superiores a los 50 millones y con un 3,5 % los patrimonios de más de 100 millones de euros, de forma que la mayor parte del impuesto recaiga sobre los mil patrimonios más altos del país. Este impuesto sustituirá al actual impuesto sobre el patrimonio» y que luego finalmente en su actividad parlamentaria defiendan que sólo se gravará un 1% de los patrimonios de más de un millón de euros.</p>

      <p class="list-cap">Criterios de evaluación ¿Cumplido/Parcialmente cumplido?</p>
      <ul>
        <li>Si el compromiso recoge una propuesta de acción concreta y explícita, una PNL no es suficiente</li>
        <li>Si el compromiso es laxo (se impulsará, se promoverá, etc) seguramente sea un compromiso peor, pero la PNL sí puede estar a la altura y, por tanto, debería ser un cumplido</li>
      </ul>

      <h3>No cumplido (NC)</h3>

      <p>Esta valoración se otorgará a los compromisos adquiridos que no se han abordado en el periodo en el que se ha llevado a cabo el monitoreo o aquellos actores que habiéndose comprometido a ello, no hayan apoyado iniciativas presentadas por otros actores (el Gobierno u otro partido político).</p>

      <p>En el caso de que Gobierno o partidos políticos hayan realizado nuevos compromisos, se establecerá un periodo de tres meses como margen de tiempo para comenzar a exigir su cumplimiento. Antes de ese periodo se considera que no ha pasado suficiente tiempo como para valorar.</p>

