      <p>En este caso, hay que tener en cuenta el tipo de acto parlamentario únicamente para ver qué tres puntuaciones le podemos dar, es decir, si es un proyecto de ley o una proposición de ley la máxima puntuación si el contenido es exactamente igual que el compromiso será 15 pero si es una PNL y el contenido es exactamente igual, su puntuación máxima es un 3.  En el caso de que el contenido no se parezca a nada  o se parezca muy poco a la propuesta, se le otorgará un 0. En el caso de que el contenido tenga similitudes respecto al objetivo, ámbito de actuación y contexto se le otorgará el 50% de la puntuación máxima.  Si por otra parte, el contenido es exactamente igual al compromiso previo, se le otorgará la puntuación máxima.</p>

