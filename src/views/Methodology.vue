      <p class="list-cap">En el caso del <strong>Gobierno</strong> se usarán sus:</p>
      <ul>
        <li>Reales Decretos</li>
        <li>Proyectos de ley (incluidos los PGE)</li>
      </ul>

      <p class="list-cap">En el caso de los <strong>Grupos Parlamentarios</strong> se usarán sus:</p>
      <ul>
        <li>
          Actividad legislativa
          <ul>
            <li>Proposición de ley</li>
            <li>Enmiendas (incluidas las introducidas en la tramitación de los PGE)</li>
          </ul>
        </li>
        <li>
          Actividad de orientación política
          <ul>
            <li>Proposiciones No de Ley y Mociones consecuencia de Interpelación Urgente</li>
            <li>Dictámenes</li>
          </ul>
        </li>
        <li>Apoyo a las iniciativas del resto de los grupos parlamentarios</li>
      </ul>

      <p>En ambos casos, vamos a centrar nuestro análisis en iniciativas propositivas que se impulsan de manera proactiva por parte del Gobierno y de los partidos políticos dado que son el mejor indicador del compromiso de cumplir sus promesas y acuerdos adquiridos previamente. Aclaramos que las Preguntas y las Interpelaciones o mociones como parte de la función de control al Gobierno no serán objeto de seguimiento del proyecto puesto que nos vamos a centrar en el carácter propositivo de los grupos parlamentarios en comparación con los compromisos previamente adquiridos. En el caso de las <strong>proposiciones no de ley, mociones y resoluciones</strong> son actos no legislativos por los que los partidos políticos manifiestan su postura política en el Congreso sobre un tema o problema determinado y a través de los que podemos analizar el cumplimiento de los compromisos previamente adquiridos.</p>

      <p>En relación con el uso de las cuentas de redes sociales (Twitter, Facebook e Instagram) del Gobierno, de los partidos políticos y de las personas referentes dentro de los partidos y el Gobierno como fuente de información para la evaluación de los compromisos escogidos, se tomará el posicionamiento público de esta persona como equivalente del posicionamiento del partido o Gobierno al que representa.</p>

      <p>En el caso del Gobierno se vigilarán las RRSS de: el Presidente y de lxs Vicepresidentxs, Ministr@, Secretari@ de Eº que tengan que ver con la temática a la que se le de seguimiento.</p>

      <p>En el caso de los partidos políticos se vigilarán las RRSS del partido, de su máximo representante (líder del partido), de su portavoz y de la persona que en ese partido sea referente de la temática.</p>

