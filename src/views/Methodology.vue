      <h3>¿Cómo evaluamos el grado de cumplimiento?</h3>
      <p>Tras la evaluación de los compromisos del Gobierno y de los grupos políticos en relación a su actividad parlamentaria se les valorará como: Cumplido, Parcialmente Cumplido y No Cumplido.</p>
      <p>En el caso de la segunda cuestión que vamos a valorar, tiene que ver con el grado de cumplimiento de los compromisos electorales. En este caso, se evalúa tanto la coherencia entre compromiso electoral y la  iniciativa parlamentaria respecto a la ambición y respecto al contenido.</p>
      <p>Con el grado de ambición nos referimos a la herramienta parlamentaria que han puesto en marcha (proposición de ley, enmienda, PNL, etc)en la actividad parlamentaria impulsada tiene el mismo grado de ambición que el compromiso electoral. Es decir, es más ambiciosa una propuesta o proposición de ley que una enmienda, o una PNL.</p>

      <p class="list-cap">De mayor a menor ambición:</p>
      <ul>
        <li>Real Decreto (sólo el Gobierno)</li>
        <li>Proyectos de ley (sólo el Gobierno)</li>
        <li>Proposiciones de ley (PL) (máxima ambición para </li>
        <li>Enmiendas a PL y a Proyectos ley</li>
        <li>Interpelaciones(I) y Mociones</li>
        <li>Enmiendas a Mociones </li>
        <li>Proposiciones No de Ley (PNL)</li>
        <li>Enmiendas a PNL</li>
      </ul>

      <p>Con el contenido nos referimos al grado de similitud en el contenido del acto parlamentario respecto al compromiso electoral o acuerdo de Gobierno.</p>

