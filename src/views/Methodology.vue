      <h3>Criterios de evaluación de las iniciativas</h3>
      <p>En principio, evaluamos las iniciativas tanto aprobadas como aquellas en tramitación porque hay partidos políticos que por su escasa representación en el Congreso de los Diputados, es difícil que les vayan a aprobar sus iniciativas parlamentarias aunque eso no significa que no estén intentando cumplir sus compromisos.</p>

      <p class="list-cap">En este caso, diferenciamos dos conceptos:</p>
      <ul>
        <li>Relevancia política (medida en función de su capacidad de aprobar o influir con contenido propio en los textos que finalmente se aprobaban)</li>
        <li>Compromiso (y cumplimiento de los mismos)</li>
      </ul>

      <h3>¿Cómo evaluamos la relevancia política?</h3>
